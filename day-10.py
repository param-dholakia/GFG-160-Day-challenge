class Solution:
    ##Complete this function
    #Function to find the sum of contiguous subarray with maximum sum.
    def maxSubArraySum(self,arr):
        ##Your code here
        msf=arr[0]
        meh=arr[0]
        for i in range(1,len(arr)):
            msf=max(arr[i],msf+arr[i])
            meh=max(msf,meh)
        return meh

